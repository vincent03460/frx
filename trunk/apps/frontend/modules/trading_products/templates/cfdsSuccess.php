        <p>
            In order to view all available CFDs in the Market Watch window inside the Metatrader trading platform, just move your mouse cursor inside the Metatrader over the symbols located in the Market Watch window, right click your mouse on any of the symbols and select &quot;Show All&quot; from the popup menu.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Execution Spreads</h3>

        <p>
            As a result of our Straight Through Processing, the spreads vary, which includes times of high market volatility, before and after major economic releases, and following other important news and events. Spreads and liquidity will reflect the prevailing interbank market liquidity.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Trading Minimums</h3>

        <p>
            Trading of CFDs takes place in &quot;lots&quot;.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Standard Accounts</h3>

        <p>
            Minimum transaction size for standard accounts is 1/100th standard sized lot (.01). The minimum deposit for a Standard Account is $100 USD.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Premium Accounts</h3>

        <p>
            Minimum transaction size for institutional accounts is 1/10th standard sized lot (.10). The minimum deposit for an Institutional Account is $10,000 USD.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Price Quotes</h3>

        <p>
            Trades are quoted via transparent &quot;Straight Through Processing&quot;; live prices are continuously published to clients via our trading platform, and traders can at any time click on the current bid or offer and execute a market trade. Trades are executed via &quot;Market Execution&quot; and the prices are updated automatically as market conditions dictate.</p>
