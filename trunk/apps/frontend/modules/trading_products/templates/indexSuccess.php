        <h3 style="margin-top:5px; line-height:30px;">
            Price Quotes</h3>

        <p>
            Trades are quoted via transparent &quot;Straight Through Processing&quot;; live prices are continuously published to clients via our trading platform, and traders can at any time click on the current bid or offer and execute a market trade. Trades are executed via &quot;Market Execution&quot; and the prices are updated automatically as market conditions dictate.</p>

        <h3 style="margin-top:5px; line-height:30px;">
            Trading over the Internet</h3>

        <p>
            Odin Capital forex offers its clients a simple procedure for executing a trade. Through our trading platform, just enter the number of lots and then click on the bid (buy) or offer (sell) for the currency pair you wish to trade. Your trade is automatically executed. The trading software calculates the initial margin requirement based upon the notional amount of the trade, and if sufficient funds are available in your account, will accept the transaction. Trades are confirmed online, normally within one second, and the system instantaneously updates both your open position and calculates your current P&amp;L.</p>

        <h3 style="margin-top:5px; line-height:30px;">
            Margin</h3>

        <p>
            The maximum available margin is 0.1%, or 1:1000 leverage for Micro accounts. Standard accounts can trade with 0.2%, or 1:500 leverage. Premium accounts can trade with 0.5%, or 1:200 leverage. STP/ECN accounts can trade with 1%,or 1:100 leverage.Odin Capital forex Traders have the option of using a lower degree of leverage upon request. If maximum leverage is employed, traders must maintain the minimum margin requirement on their open positions at all times. It is the customer&#39;s responsibility to monitor his/her margin account balance. Odin Capital forex has the right to liquidate any or all open positions whenever a trader&#39;s minimum margin requirement is not maintained.</p>

        <p>
            This is an important risk management feature designed to strictly limit trading losses in an account. The minimum margin requirement is approximately $25 per lot of 10,000 units of base currency in a standard account with 400:1 leverage. The requirements for leverage may vary with account size or market conditions, and may be changed from time to time at the sole discretion of Odin Capital forex or upon request.Note: margin cannot be changed while holding open positions.</p>

        <h3 style="margin-top:5px; line-height:30px;">
            Rollovers</h3>

        <p>
            Odin Capital forex clients have the opportunity to earn interest on positions left open overnight at all margin levels. The amount of interest that can be earned depends on the direction of the open position and the interest rate differential between the two currencies involved. For example, UK interest rates are significantly higher than Japan&#39;s, so if a trader is long GBP/JPY (i.e. holding British Pounds), they can earn interest on the roll. Conversely, if a trader is short GBP/JPY (i.e. holding yen) they will pay interest on the rollover.</p>

        <h3 style="margin-top:5px; line-height:30px;">
            Confirmations</h3>

        <p>
            Trades are confirmed on screen, typically within one second. Full transaction details may be accessed on screen as well, including date, time, rate, and notional amount bought and sold, USD value, and reference number.</p>

        <h3 style="margin-top:5px; line-height:30px;">
