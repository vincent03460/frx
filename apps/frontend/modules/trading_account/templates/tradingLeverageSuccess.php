    <div class="dollorsaccount">
        <p>
            The size of the leverage in our company ranges up to 1:1000 *. Due to this condition Odin Capital forex margin requirements are some of the most versatile to date.</p>

        <div style="margin:10px 0 10px 40px; background:#EEE;border-left:1px solid #DDD; padding:15px;line-height:24px;">
            Leverage represents the ratio of debt to equity. In addition, the terms &quot;financial leverage&quot; and &quot;credit leverage&quot; are widely used to refer to this concept.
        </div>
        <p>
            At Odin Capital forex the value of the maximum possible leverage depends on the type of order execution that is relevant for the given trading account. In Forex Micro and Forex Standard trading accounts, orders are executed using Instant execution technology. Market execution technology, which ensures direct access to interbank liquidity, is used in Forex ECN accounts.</p>
        <h4 style="fony-size:14px;border-bottom:2px solid #C00;">
            Leverage and rules for its usage for Odin Capital forex clients</h4>

        <p>
            In 2012, Odin Capital forex made ​​significant changes to the conditions of how it provides financial leverage for Forex Micro accounts. If the amount of client funds in such an account does not exceed 100 USD (or its equivalent in another currency), then this account is eligible for a maximum financial leverage of 1:1000. If the amount of funds in the Forex Micro account is in the 100 USD to 1,000 USD range (or its equivalent in another currency), the maximum amount of financial leverage available to clients will be 1:500. Such a significant reduction in margin requirements allows traders to effectively implement a wide range of trading strategies.</p>

        <p>
            Margin requirements increase when the funds available in a client&#39;s account increase. This is due to the increasing costs of hedging open orders. Consequently, the size of the provided leverage changes.</p>

