        <p>
            For an exact understanding of the principles of ECN broker working, let us remind ourselves how the market value of any financial asset is formulated on FOREX - the dynamic of value is subordinate to the law of supply and demand. An increase in price always follows increased demand and vice versa. On FOREX (as in any other market) some participants wish to buy, others want to sell.</p>

        <div style="background:#eee; border-left:1px solid #CCC;padding:15px;line-height:24px;margin:10px 0 10px 50px;">
            Suppose for example that one trader has decided to buy 10,000 EUR at a rate of 1.2500 USD for 1 EUR, and another at the same time wanted to sell the same amount of EUR at the same price. Executing the bids of these traders, the ECN broker transmits their orders into the system, where they are then executed (overlapping each other).
        </div>
        <p>
            ECN brings together several major FOREX participants, significantly widening the flow of liquidity on financial assets. Accordingly traders receive access to the interbank market. The mechanism for ensuring this access will be looked at in more detail later.</p>

        <p>
            Having implemented the technology for transmitting its clients&#39; bids (for their subsequent execution) into the trading systems of the major market participants, Odin Capital forex has gained the opportunity to invite traders to exploit all the advantages of ECN in their work.</p>

        <p>
            It can be said that ECN is an interbank market in which the execution of bids not only precludes a conflict of interest between brokers and traders but also, in the absence of a monopoly on pricing, allows the execution of clients&#39; bids at best prices. A regular ECN broker receives income on his commission account which clients pay for each transaction, and does not take any part in the formation of the pricing on a specific trading instrument. Current prices are primarily determined by the bids of participants in the system.</p>

