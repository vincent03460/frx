        <h3 style="margin:0; line-height:40px;">
            Trading Minimums</h3>
        <p>
            Trading of gold and silver takes place in &quot;lots&quot;. 1 lot of gold = 100oz and 1 lot of silver = 5000oz. Trading of gold starts at 1oz = 0.01 lot and silver from 50oz = 0.01 lot.</p>
        <h3 style="margin:0; line-height:40px;">
            Price Quotes</h3>
        <p>
            Trades are quoted via transparent &quot;Straight Through Processing&quot;; live prices are continuously published to clients via our trading platform, and traders can at any time click on the current bid or offer and execute a market trade. Trades are executed via &quot;Market Execution&quot; and the prices are updated automatically as market conditions dictate.<br />
            Price quotes of spot gold and silver are similar to price quotes of Forex currency pairs. For example, spot gold traded against the US dollar is GOLD.<br />
            Spot prices of gold and silver are quoted internationally in US dollars per troy ounce, so for a quote of GOLD 1300.50, 1oz of gold is equal to $1300.50<br />
            Increasing quote for gold means strengthening of gold value. Conversely, decreasing quote for gold means the gold is worth fewer dollars than before.<br />
            A typical quote you might receive for spot gold is 1385.80/1386.30 (bid/ask). This means that you could sell at 1385.80, or buy at 1386.30. The difference between the bid and ask prices is the spread (1385.80-1386.30) or 0.50.</p>
        <h3 style="margin:0; line-height:40px;">
            Calculating Profit &amp; Loss</h3>
        <p>
            For gold, 1 lot is 100 troy ounces, and the smallest price increment (5 pips) is 0.05. This means that a one increment movement in the gold price represents a $5 price movement for each lot that you are trading. For silver, 1 lot is 5000 troy ounces, and the smallest price increment (pip) is 0.01. This means that a one increment movement in the silver price represents a $50 price movement for each lot that you are trading.</p>
        <h3 style="margin:0; line-height:40px;">
            As an example:</h3>
        <p style="margin-left:50px;border-left:1px solid #DDD;background:#f5f5f5;padding:15px;">
            You decide to buy 1 lot of XAU/USD (spot gold) at 1383.30. A few minutes later, the bid (or sell) price has risen to 1383.90, and you decide to exit your trade.</p>
        <h3 style="margin:0; line-height:40px;">
            Calculating your profit:</h3>
        <p>
            You bought 1 lot at 1383.30<br />
            You sold 1 lot at 1383.90 a difference of 0.60, or 60 pips<br />
            Your trade has therefore made 60 pips x $1, equal to $60.</p>
        <h3 style="margin:0; line-height:40px;">
            If the price had moved against you to 1382.80, your loss would be calculated as follows:</h3>
        <p>
            You bought 1 lot at 1383.30<br />
            You sold 1 lot at 1382.80, a difference of 50 pips.<br />
            Your trade has therefore made a loss of 50 pips x $1 per pip, equals to $50.</p>
