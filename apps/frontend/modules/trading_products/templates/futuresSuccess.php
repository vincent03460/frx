    <h3>Futures</h3>

    <div class="dollorsaccount">
        <p>
            Our clients can gain access to the major indices as well as major commodity and energy futures through CFDs offered by Odin Capital forex.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Key Benefits of Trading CFDs on Futures with Odin Capital forex:</h3>
        <ul>
            <li>
                Ability to go long or short.
            </li>
            <li>
                Exposure to major equity indices with a fraction of the required exchange margin.
            </li>
            <li>
                Access to agricultural commodities and possibility to monitor changes based on global food prices.
            </li>
            <li>
                Trade crude oil and natural gas futures &ndash; all within the same trading account.
            </li>
        </ul>
        <h3 style="margin: 0px; line-height: 40px;">
            Pricing</h3>

        <p>
            Odin Capital forex provides prices on its CFDs that closely track the real prices of the futures contracts traded on the exchanges, and prices are quoted in the underlying currency of the future contract.Odin Capital forex charges a fraction of the commission charged by the underlying exchange and requires a smaller deposited margin than that of the exchange.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Trading Times</h3>

        <p>
            Trading times for all futures CFDs are exactly the same as the trading times of the underlying exchange on which futures contracts are traded.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Order Sizes</h3>

        <p>
            Order sizes depend on the individual underlying futures contract, details of which are provided below and start at 0.1 lots on each futures contract.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Swap/Rollover</h3>

        <p>
            If a trade is kept open overnight, then there is a swap/rollover cost/income calculated on that position. The cost or income is calculated as the overnight interest rate differential between the two currencies plus the commission charged by the company on which the position is held, depending on the type of the position (long/short).</p>

        <p>
            The &rsquo;swap&rsquo; is charged automatically on the account of the client and is converted into the currency that the account is denominated in. The &lsquo;swap&rsquo; is charged at 21:59 server time. From Friday to Monday swap is charged once. From Wednesday to Thursday swap is charged at triple rate.</p>

        <h3 style="margin: 0px; line-height: 40px;">
            Futures Contract Details</h3>

        <p>
            Please note that there is a floating spread for trading in CFDs on futures which is not listed.</p>
